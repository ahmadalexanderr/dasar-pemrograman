# Deskripsi : menghitung nilai pangkat 3 dari sebuah masukan integer

# Definisi dan spesifikasi dari fungsi pangkat3 bernama fx3_v1 (x) adalah:
# fx3 : integer --> integer
#   fx3v1 (x) menghitung pangkat dua dari x, sebuah bilangan integer

# Realisasi
def fx3v1(x):
    return x * x * x


# Aplikasi
print(fx3v1(4))
print(fx3v1(-10))
